package lecture.chapter3;

public class DataTypeExamples {

    public static void main(String[] args) {

        // Deklaration einer boolean Variable
        boolean isTired;

        // Initialisierung der Variable isTired auf "true"
        isTired = true; // true / false

        // Deklaration und Initialisierung der Ganzzahlen Variable "numberA"
        int numberA = 5;
        double numberB = 3.41;

        numberA = 98;

        // Deklaration & Initialisierung der Konstanten PI
        final double PI = 3.14;

        // Ändern von Konstanten nicht möglich
        // PI = 5.41;

        // Ausgabe von numberA und PI
        System.out.println(numberA);
        System.out.println(PI);

    }
}